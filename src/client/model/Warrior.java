package client.model;

public class Warrior {
    public String name;
    public int percentage;
    public String type;
    public int fuerza;
    public int resistencia;
    public int sanidad;
    public String image;

    public Warrior(String name, int percentage, String type, int fuerza, int resistencia, int sanidad, String image) {
        this.name = name;
        this.percentage = percentage;
        this.type = type;
        this.fuerza = fuerza;
        this.resistencia = resistencia;
        this.sanidad = sanidad;
        this.image = image;
    }



}

